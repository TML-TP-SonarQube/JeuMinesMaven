package mines;

/**
 * The {@code Corner} enum represents the possible corners of a cell on the board game.
 * The values of this enum represent each corner of a cell on the board.
 * <p>The possible values are:</p>
 * <ul>
 *     <li>{@code TOP_LEFT}</li>
 *     <li>{@code TOP}</li>
 *     <li>{@code TOP_RIGHT}</li>
 *     <li>{@code LEFT}</li>
 *     <li>{@code RIGHT}</li>
 *     <li>{@code BOTTOM_LEFT}</li>
 *     <li>{@code BOTTOM}</li>
 *     <li>{@code BOTTOM_RIGHT}</li>
 * </ul>
 */
public enum Corner {
    TOP_LEFT,
    TOP,
    TOP_RIGHT,
    LEFT,
    RIGHT,
    BOTTOM_LEFT,
    BOTTOM,
    BOTTOM_RIGHT
}
